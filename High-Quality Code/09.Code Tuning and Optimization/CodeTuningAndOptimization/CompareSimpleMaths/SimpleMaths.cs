namespace CompareSimpleMaths
{
    public static class SimpleMaths
    {
        public static int Add(int first, int second)
        {
            int result = first + second;

            return result;
        }

        public static long Add(long first, long second)
        {
            long result = first + second;

            return result;
        }

        public static float Add(float first, float second)
        {
            float result = first + second;

            return result;
        }

        public static double Add(double first, double second)
        {
            double result = first + second;

            return result;
        }

        public static decimal Add(decimal first, decimal second)
        {
            decimal result = first + second;

            return result;
        }

        public static int Substract(int first, int second)
        {
            int result = first - second;

            return result;
        }

        public static long Substract(long first, long second)
        {
            long result = first - second;

            return result;
        }

        public static float Substract(float first, float second)
        {
            float result = first - second;

            return result;
        }

        public static double Substract(double first, double second)
        {
            double result = first - second;

            return result;
        }

        public static decimal Substract(decimal first, decimal second)
        {
            decimal result = first - second;

            return result;
        }

        public static int Increment(int number)
        {
            int result = number++;

            return result;
        }

        public static long Increment(long number)
        {
            long result = number++;

            return result;
        }

        public static float Increment(float number)
        {
            float result = number++;

            return result;
        }

        public static double Increment(double number)
        {
            double result = number++;

            return result;
        }

        public static decimal Increment(decimal number)
        {
            decimal result = number++;

            return result;
        }

        public static int Multiply(int first, int second)
        {
            int result = first * second;

            return result;
        }

        public static long Multiply(long first, long second)
        {
            long result = first * second;

            return result;
        }

        public static float Multiply(float first, float second)
        {
            float result = first * second;

            return result;
        }

        public static double Multiply(double first, double second)
        {
            double result = first * second;

            return result;
        }

        public static decimal Multiply(decimal first, decimal second)
        {
            decimal result = first * second;

            return result;
        }

        public static int Divide(int first, int second)
        {
            int result = first / second;

            return result;
        }

        public static long Divide(long first, long second)
        {
            long result = first / second;

            return result;
        }

        public static float Divide(float first, float second)
        {
            float result = first / second;

            return result;
        }

        public static double Divide(double first, double second)
        {
            double result = first / second;

            return result;
        }

        public static decimal Divide(decimal first, decimal second)
        {
            decimal result = first / second;

            return result;
        }
    }
